// Regenerates ui/src/locale-catalog.ts from the Unicode CLDR "available locales"
// list — the authoritative set of every language and regional/script locale CLDR
// has data for (base languages like `es`, plus variants like `es-MX`, `zh-Hans`).
//
// Run manually after a CLDR bump:  node scripts/gen-locale-catalog.mjs
//
// We ship the full set of codes; the UI shows only those the browser's Intl can
// name (filtered at runtime), and names/flags/RTL all resolve at runtime via
// languages.ts. Keeping every code here means power users can still type an obscure
// but valid code (e.g. "aa") and have it recognised, even if it isn't in the list.

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const SOURCE = "https://raw.githubusercontent.com/unicode-org/cldr-json/main/cldr-json/cldr-core/availableLocales.json";

const here = dirname(fileURLToPath(import.meta.url));
const out = resolve(here, "../ui/src/locale-catalog.ts");

const res = await fetch(SOURCE);
if (!res.ok) throw new Error(`Failed to fetch CLDR locales: ${res.status}`);
const { availableLocales } = await res.json();

// `und` (undetermined) is not a real language users would translate into.
const base = availableLocales.full.filter((c) => c !== "und");

// CLDR omits the default-region form of each language (it treats bare `pt` as
// Brazilian, `en` as US…). Translation projects, though, name files explicitly
// `pt-BR`/`en-US`, so add each base language's maximized region back in.
const defaults = [];
for (const code of base) {
  if (code.includes("-")) continue;
  try {
    const region = new Intl.Locale(code).maximize().region;
    if (region) defaults.push(`${code}-${region}`);
  } catch {
    // skip codes Intl can't maximize
  }
}

const codes = [...new Set([...base, ...defaults])]
  // Drop the `-001` (UN M49 "World") variants: they're region-neutral duplicates of
  // the base language (e.g. `eo-001` "Esperanto (World)" ≡ `eo`, `en-001` ≡ `en`).
  // Meaningful macro-regions like `es-419` (Latin America) and `en-150` (Europe) stay.
  .filter((c) => !/-001$/.test(c))
  .sort((a, b) => a.localeCompare(b, "en"));

const lines = codes.map((c) => `  "${c}",`).join("\n");

const body = `// AUTO-GENERATED by scripts/gen-locale-catalog.mjs — do not edit by hand.
// Source: Unicode CLDR availableLocales ("full"). Every language and locale CLDR
// knows. Codes are canonical BCP-47; names/flags/RTL are resolved at runtime via
// Intl (see languages.ts). The "Add language" helper dropdown shows only the codes
// the browser can name; obscure-but-valid codes still live here so typing one is
// recognised, and made-up codes (e.g. "yoda") can be added even though they aren't.

const CATALOG = [
${lines}
] as const;

export const LOCALE_CATALOG: readonly string[] = CATALOG;

const CATALOG_SET = new Set(CATALOG.map((c) => c.toLowerCase()));

// True when \`code\` is a recognised BCP-47 locale (case-insensitive), as opposed to
// a custom/invented one the user typed.
export function isKnownLocale(code: string): boolean {
  return CATALOG_SET.has(code.trim().toLowerCase());
}
`;

writeFileSync(out, body);
console.log(`Wrote ${codes.length} locales to ${out}`);
